        string.format( "%2.2f", b1[ b1idx ][3] ) )
    set_real_value( variables.b1_atmtemp,
        string.format( "%2.2f", b1[ b1idx ][4] ) )
    set_real_value( variables.b1_seatemp,
        string.format( "%2.2f", b1[ b1idx ][5] ) )

    b1idx = b1idx + 1
    if b1idx > table.getn( b1 ) then
        b1idx = 1
    end

    -- b2
    set_real_value( variables.b2_north,
        string.format( "%2.2f", b2[ b2idx ][1] ) )
    set_real_value( variables.b2_east,
        string.format( "%2.2f", b2[ b2idx ][2] ) )
    set_real_value( variables.b2_pressure,
        string.format( "%2.2f", b2[ b2idx ][3] ) )
    set_real_value( variables.b2_atmtemp,
        string.format( "%2.2f", b2[ b2idx ][4] ) )
    set_real_value( variables.b2_seatemp,
        string.format( "%2.2f", b2[ b2idx ][5] ) )

    b2idx = b2idx + 1
    if b2idx > table.getn( b2 ) then
        b2idx = 1
    end

    sync_real_values()

    if not sleep( 2 * 1000 * 1000 ) then
        break
    end

end

